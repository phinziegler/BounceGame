export default class Vector {
    constructor(x, y) {
        this.x = x;
        this.y = y;
    }

    // GET-SET X
    get x() {
        return this.x;
    }
    set x(x) {
        this.x = x;
    }

    // GET-SET Y
    get y() {
        return this.y;
    }
    set y() {
        this.y = y;
    }
}